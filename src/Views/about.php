<h1>Over dit spel</h1>

<section class="card">
  <h2>Wat is dit?</h2>
  <p>
    Een zelf te hosten implementatie van twee serious card games voor
    threat modeling: <strong>Elevation of Privilege</strong> (STRIDE) en
    <strong>LINDDUN GO</strong> (privacy). Gebouwd in PHP + MySQL en
    bedoeld voor educatief gebruik.
  </p>
</section>

